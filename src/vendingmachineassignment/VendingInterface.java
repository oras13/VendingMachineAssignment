    private void jButtonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmActionPerformed
        if (moneyIn >= totalPrice){
            change = moneyIn - totalPrice;
            change = roundMoney(change);
            jTextChange.setText(change + "");
            totalMoney += totalPrice;
            totalMoney = roundMoney(totalMoney);
            moneyIn = 0.0;
            totalPrice = 0.0;
            listMoneyIn.removeAll();
            listProductsSelect.removeAll();
            }else if(moneyIn < totalPrice){
                JOptionPane.showMessageDialog(frame, "Not enough money!.");
        }
        
    }//GEN-LAST:event_jButtonConfirmActionPerformed

