    private void jButtonPepsiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPepsiActionPerformed
        if (pepsiStock > 0){
            pepsiStock --;
            listProductsSelect.add("Pepsi");
            totalPrice += 1.7;
            totalPrice = roundMoney(totalPrice);
            jTextTotalCost.setText(totalPrice + "");
            jPepsiAvailable.setSelected(true);
        }else {
            jPepsiAvailable.setSelected(false);
            JOptionPane.showMessageDialog(frame, "Out of Stock!");
        }
    }//GEN-LAST:event_jButtonPepsiActionPerformed

